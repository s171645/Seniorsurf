
<h2><strong>Problemstilling</strong></h2>

<p> 
	Vår problemstilling er å produsere en lett og brukervennlig hjemmeside for å hjelpe eldre som vil på nett. 
	Målgruppen vi fokuserer på er aldersgruppe 50+.
	I startfasen når vi gjorde undersøkelser for å finne en passende problemstilling, ble blant annet denne artiklene av spesiell interesse for oss: 
</p>
 
<p>
	<em>
		<strong> 
			”Tidligere undersøkelser har vist at 4 av 10 som har gått av med  pensjon  angrer.
			Mange av dem oppgir usikkerhet knyttet til data som en viktig forklaring på hvorfor de valgte å tre ut av arbeidslivet tidlig.
			Dermed øker pensjonsutgiftene og samfunnet går glipp av viktig kompetanse.
			” og ”Av Norges 1,25 mill seniorer, de over 55år, så er det fortsatt ca 500 000 som ikke har kunnskaper om eller bruker internett.”
		</strong>
	</em>
</p>


<p>
	Dette var en av grunnene som inspirerte oss til å gå for denne problemstillingen.
	Vi vil gi eldre digital kompetanse, fordi denne kompetansen er definert som en basisferdighet.  
	Det vil si at alle skal beherske et minimum av digitale ferdigheter.
	Vi ser at den yngre generasjonen får dette gjennom skolen,
	mens de som er ved slutten av arbeidslivet eller allerede pensjonert har få valg av offentlig tilbud om IT opplæring. 
	Dette er de viktigste årsakene til at vi lagde nettsiden.
</p>

