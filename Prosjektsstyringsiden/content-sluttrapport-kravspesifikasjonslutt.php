<h2><strong>Kravspesifikasjon</strong></h2>
<strong>WCAG</strong>


<p>
	WCAG er retningslinjer som innholder spesifikke krav som skal gi nettbrukerne en bedre og enklere nettside. 
	• Krav som må oppfylles, utgjør ”nivå 1” eller ”A”.
	• Krav som bør oppfylles, utgjør ”nivå 2” eller ”AA” .
	• Krav som kan oppfylles og som forbedrer tilgjengeligheten ytterligere, utgjør ”nivå 3” eller ”AAA”.
</p>

<p>
	Vi har forsøkt å oppfylle så mange krav så mulig. En del av de kravene er vanskelig å forstå, men vi har innfridd de fleste kravene på A og AA og noen på AAA.
</p>

<p>
	Kravene er hentet fra det norske <a href="http://wcag.difi.no/hoveddokumentet-pa-norsk">WCAG 2.0 siden til Difi</a>   
</p>

