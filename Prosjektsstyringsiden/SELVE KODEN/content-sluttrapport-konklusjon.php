
<h2><strong>Konklusjon</strong></h2>

<p>
	Vi ville lage en informasjonsside til en brukergruppe som ikke kan så mye med internett,
	og vi valgte å fokusere på eldre som brukere. 
</p>

<p>
	<strong>Hvorfor akkurat eldre?</strong> Hvis vi deler opp nye brukere av data og internett,
	kan man i grove trekk si at det er barn, eldre og kanskje innvandrere som trenger opplæring.
	Barn lærer tidlig å bruke pc, og allerede i grunnskolen har de datatimer hvor de lærer ved hjelp av spill og interaktiv underholdning,
	når de kommer litt opp i klassetrinnene vil de lære bruken av internett.
	Innvandrere som kommer til Norge får tilbud om datakurs hos Nav.
	Og mange av norskkursene som blir arrangert, gjøres på pc, så de som ikke kan data, blir da vant til bruken av pc og internett på den måten. 
</p>

<p>
	<strong>Men hva med de eldre?</strong> Enkelte kommuner arrangerer data/nett kurs for eldre en eller to ganger i året.
	Det er ikke mye.
</p>

<p>
	Vi snakket med en eldre herre som hadde kjøpt sin første laptop i en alder av 81 år.
	Villy hadde bestemt seg for at han skulle lære å bruke data,
	og siden kommunen ikke hadde noe særlig kurs å tilby, kjøpte han en laptop, og ville lære seg selv.
	‘Dataen i seg selv, var grei å finne ut av’, sier Villy, ‘Men så var det denne internetten’ sier han oppgitt!
</p>

<p>
	Altså kan Internett være en veldig vanskelig ting å sette seg inn i for en nybegynner.
	Det var med den problemstillingen ideen vår om en hjelpeside for eldre kom til liv.
	Vi så et behov for en nettside som enkelt å greit kan lære en eldre person å forstå internett litt bedre.  
</p>

<p>
	Igjennom store deler av semestret har vi jobbet med Seniorsurf.
	Vi har laget en enkel funksjonell side som er tilpasset brukergruppen vår,
	og vi er fornøyd med resultatet vårt. Vi har igjennom deler av prosessen vært i nær kontakt med Furuset seniorsenter,
	som har vært våre testere. Derfra har vi fått skryt over ting som har vært bra,
	og fått tilbakemelding på ting de syntes kunne vært bedre og som vi da har fikset på.
	På den måten har vi kunnet tilpasse siden vår veldig bra,
	og vi har fått ros av sluttproduktet vårt fra beboerne på senteret,
	noe vi annser som den beste tilbakemelding, da de er potensielle framtidige brukere av seniorsurf.
</p>

<p>
	Ting vi kunne gjort bedre er nok å kommet raskere i gang.
	Ved første leveranse fokuserte vi altfor mye på prosjektstyringssiden,
	og vi kom oss ikke i gang med hovedsiden så raskt som vi burde. Selv om alt ble ferdig,
	skulle man alltids ønske at man hadde mer tid til å gjøre det enda bedre,
	noe vi kanskje kunne gjort hadde vi startet litt tidligere med hovedsiden.
	Underveis slet vi også en god del med å forstå GIT,
	vi syntes det spiste mye av tid og fokus på hovedarbeidet,
	og vi har slitt med det fra start til slutt. 
</p>

<p>
	Ellers har prosjektet og gruppearbeidet har gått overraskende bra til tross for den lille tiden man har til hvert fag når man studerer.
	Vi har samarbeidet godt, og alle har vist stor interesse for nettsiden vi valgte å lage.
	Vi har vært litt uheldig med en del sykdom i gruppa,
	men har klart å samarbeide godt likevell via epost, msn og dropbox.
	Vi har alle holdt oss til tidsfrister, og har fått alle leveransene inn i tide.
	Gjennom prosjektarbeid har vi lært mye, både om samarbeid,
	og hvor viktig det er å overholde tidsfrister,
	og ikke minst har vi lært at det å jobbe jevnt og trutt faktisk lønner seg til slutt:D
	Vi er alle enige om at vi nå har fått et innblikk i hvordan et prosjekt foregår,
	og er nå mer forberedt på hva som venter når vi begynner å arbeide etter skolen.
</p>

