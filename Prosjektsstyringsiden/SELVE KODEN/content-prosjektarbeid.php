<h2>Evaluering av prosjektarbeidet så langt:</h2>

<p>
	<strong>Hvordan har samarbeidet i gruppa gått?</strong>
</p>

<p>	
	Samarbeidet i gruppa har gått kjempebra så langt.
	Ingen av oss har jobbet i prosjekt tidligere,
	så vi har alle måttet lære oss å samarbeide og kommunisere for og komme i gang med prosjektet.
	Vi har vært veldig flinke til å jobbe sammen og vi har etter hvert blitt mer strukturert i møtene og arbeidsfordelingene.
	Vi kommuniserer og avtaler møter hver dag via e-post, sms eller dropbox,
	og alle medlemmene i gruppa deltar aktivt. Vi er alt i alt en meget velfungerende gruppe. 
</p>

<p>
	<strong>Hvordan har dere lykkes med å følge fremdriftsplanen?</strong>
</p>

<p>
	Vi har fulgt fremdriftsplanen så godt som mulig.
	Siden prosjektet er nyoppstartet, kommer man gjerne på flere ideer man vil inkludere underveis.
	Vi har kommet på flere ideer enn det som står på den originale fremdriftsplanen, og siden dette er typisk noe som skjer i et prosjekt,
	anser vi det som en positiv avsporing fra den originale planen.   
</p>

<p>
	<strong>Har det dukket opp noe spesielt underveis i prosjektet som har påvirket fremdriften, og hvordan har dere eventuelt håndtert dette?</strong>
</p>

<p>
	Det eneste som har påvirket fremdriften i prosjektet er obligene og prøvene i andre fag.
	Vi har etterhvert blitt mer strukturerte og vi skjønner at man må jobbe jevnt og trutt med alle fagene slik at vi ikke mister kontrollen.
</p>