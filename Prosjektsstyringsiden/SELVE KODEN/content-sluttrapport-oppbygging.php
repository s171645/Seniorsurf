<h2><strong>Oppbyggingen av siden</strong></h2>

<p>
	Siden vår finner du på www.SeniorSurf.org og er en domain redirect fra www.seniorsurf.byethost17.com pga. et gruppemedlem hadde tilgang til denne serveren gratis, og deretter forandret vi navnet til www.seniorsurf.org. 
	Linkene og menyene er plassert i forhold til hva som er forventet at de fleste brukere vil ha behov for.
</p>

	<h3>Index</h3>
Startsiden er bygd opp slik at den gir oversiktlige forklaringer til viktige lenker og bruksanvisning til hvordan å skifte style sheet og skrift størrelsen viss en skulle mangle java .

	<h3>videoleksjoner</h3>
Lister opp videor hvor vi viser gjennom eksempel hvordan benytte seg av diverse nyttige tjenester og populære sosiale tjenester, itilegg ligger en stepbystep forklaring til vær video  under.

	<h3>Lenker</h3>
Inneholder flere nyttige linker til tjenester kategorisiert, logikken her er å vise hva internett kan ha å by på og samtidig levere praktiske linker til brukeren. 

	<h3> Lær å bruke internett </h3>
Består av artikler som går ut på Internettsbegreper, hva er internett, hvordan søke på internett og om virus.  Dette er bare en veldig liten forklaring på et massivt inviklet stoff, det er ikke ment som en forklaring til alt, men et komme igang punkt hvor brukeren lett fattlig forstår hva som sies, blir innenforstått med det og får lyst til å lære mer på egenhånd.

	<h3> Bruker innlegg </h3>
Vi som har laget denne siden er rundt 20år, mens de som skal bruke den er over 50år,
man kommer bare så langt  selv med bruker undersøkelser og tilbakemeldinger,
spesielt i dette tilfellet der gruppen gjerne ikke vet hva som vill bli problematisk, dermed vet heller ikke vi det.
Løsningen blir å lage en tjeneste der brukeren selv legger opp tips til problemer de har hatt og
andre kanksje har. Dette blir dermest som en FAQ (noe som kunne blitt lagt opp som en egen underside i fremtiden når siden får mange nok av disse innleggene).
Her kan brukere legge inn sine egne tips og innlegg direkte opp på siden. 
Denne siden må administreres etter behov.

	<h3> Forum</h3>
Her er det forsøkt å ha en tjeneste som kan gi svar til spørrende eller hilsende på kort tid, men ikke spesielt fokusert på.
Tjenesten trenger administrator etter behov for å rydde opp forumet, kan gjerne svare på forumet, men ikke nødvendigvis. 
Kommunikasjon mellom bruker og administrator foregår først og fremst på  mail.


	<h3> Footer meny</h3>
Undermenyen består av mindre populære lenker med diverse info: kontakt info, grunnen til sidens eksistens og sidekart.

