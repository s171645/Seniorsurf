	<h3> Script, Koding og Programmering</h3>
To javascript (et for skrift størrelse og et for css valg) og scriptet for forumet er hentet fra eksterne kilder.
Alt annet er kodet spesifikt for siden, det inkluderer en hel del PHP, som er brukt til å inkluderer topp og bunn filer for å spare plass, og tid når en redigerer. 
Samt all PHP koden laget for bruker innlegg, som skapte en hel del hodebry når visse kontrollstrukturer ikke fungerte (optimalt skulle bruker innlegg ha en tidsfunksjon som sperret deg fra å legge inn flere innlegg over en viss tid og kreve at du skriver i visse tekstbokser), her måtte administratoren alikevel tatt i et tak, så det er ingen stor krise.
PHP viste seg å ikke støtte alle funskjonene på serveren vi hadde valgt, og det var derfor nødvendig og flytte kontakt skjema til studssh (vi kontaktet administrator på vår server, men han ville ikke endre på PHP innstillinger).
Frem og tilbake har versjonskontroll av filer gått veldig bra, noe kluss fra tid til annen men det får en vel bare regne med.


