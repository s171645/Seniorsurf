
		<h2><strong>Prosjektdagbok</strong></h2>
		
<h5><strong><em>Onsdag 29.09.10</em></strong></h5>
	<p>
		I dag fikk vi vite om gruppemedlemmene våre, som består av Kristina, Vijitharan, Kristoffer og Stafeni. I to måneder skal vi jobbe sammen i et prosjekt, hvor vi utvikler vår egen nettside. 
	</p>
		
<hr/>		
 <h5><strong><em>Fredag 01.10.10</em></strong></h5>  
	<p> 
		Etter å ha sendt mail til hverandre, kom vi fram til et tidspunkt på når og hvor vi skulle møtes. 
		Møtestedet var datatorget, hvor tre fra gruppen møttes. I denne perioden ble vi kjent med hverandre og vi begynte smått å diskutere om hva slags nettside vi skulle utvikle og hva vi ikke skulle lage. Vi kom fram til at ideen om nettbutikksiden skulle ligge på hyllen.		
		Dermed ble vi enige om at vi kunne komme med forslag til tema for nettside neste uke. 
	</p>
  
<hr/>  
 <h5><strong><em>Mandag 04.10.10</em></strong></h5>
	<p> 
		I dag var det tre fra gruppen som møtte opp, og vi kom med ulike forslag ti tema. Etter utveksling av ideer kom vi fram til å lage en guideside for de eldre. Etter at beslutningen var tatt, fordelte vi styringsdokumentpunktene.
		For å få mer informasjon og råd om git og ideen vår, dro vi til Simen og Siri. Der vi fikk gode råd.
		Hittil har vi blitt gode Drop – box brukere, men øver fortsatt med bruken av git. 
    </p>                 

<hr/>	
<h5><strong><em>Tirsdag 05.10.10</em></strong></h5>
	<p>
		I dag ble møtet avlyst pga. en uheldig hendelse som inntraff en i gruppen. Til tross for dette, forsatte vi å jobbe hver for oss selv med oppgavene våre.
	</p>
	
<hr/>	
<h5><strong><em>Onsdag 06.10.10</em></strong></h5>
	<p>
		I dag var alle tilstede under møtet. I møtet ble det tildelt samarbeidskontrakt som vi leste gjennom og underskrev på. 
	</p>
	
<hr/>	
<h5><strong><em>Fredag 08.10.10</em></strong></h5>
	<p>
		I dag var alle tilstede under møtet. En av gruppemedlemmene som hadde ansvaret for layouten, demonstrerte siden. Etter det jobbet vi hver for oss med oppgavene. 
	</p>
	
<hr/>	
<h5><strong><em>Mandag  11.10.10</em></strong></h5>
	<p>
		Gruppemedlemmene jobbet hver for seg selv idag. Vi hadde kontakten gjennom ved å bruke drop box. Vi er snart ferdig med leveranse 1, og imorgen skal vi møtes og gå gjennom det vi  har
		gjort hittil.  
	</p>

<hr/>
<h5><strong><em>Tirsdag  12.10.10</em></strong></h5>
	<p>	
		Idag var alle tilstede og vi jobbet sammen om leveransen på datalab 461.
		Etter leveranse 1 har vi bestemt oss for å sette oss mer inn i GIT og bruke GIT som
		kommunikasjonsmiddel og versjonshåndteringsvektøy. Hittil har vi brukt dropbox til samarbeidet vårt på grunn av at vi ikke har forstått GIT helt.
		Heldigvis har Viji fått dreisen på det, så han vil holde et lite kurs med oss andre etterhvert:)
		Grunnen til innlevering av leveranse 1 idag er at Kristina skal reise bort. Og
		som gruppe ble vi enige om å bli ferdig med oppgaven 12. oktober.
		Vi vil enda kunne gjøre litt endringer dersom det er behov for det, men siden er i dag så og si ferdig, og vi kan lett jobbe videre med denne i løpet av prosjektet.
		Midt under arbeidet måtte vi forlate datalaben, fordi det skulle være undervisning der.
		Så vi dro til sjette etasje for å forsette med felles gjennomgang av sluttproduktet i leveranse 1.
		Når vi skulle validere siden vår fikk vi en del valideringsfeil, men dette var enkle feil som ble fikset uten noe vidre problem.
		Vi er fornøyde med første leveranse, og vi har uten tvil en velfungerende gruppe!! Godt jobbet alle sammen:D
	</p>

<hr/>	
<h3><em>Mandag 18.10.10</em></h3>
	<p>
		Dagens møte gikk stort sett ut på å dobbelsjekke leveransen vår,
        fordi det er i dag oppgaven skal leveres. To fra gruppen møtte opp, og
        jobbet med hver sin oppgave.
		I dag måtte vi fikse på aktivitetsplanen og milepælsplanen,
        fordi planene manglet mer beskrivelse om fremtidriften.
        Både planene og dagboken sendte vi til Kristina,
        siden det er hun som har ansvaret for innføring av dokumentene.
        Ellers har alt godt kjempe bra, og vi gleder oss til å begynne på leveranse 2 ;)
	</p>	