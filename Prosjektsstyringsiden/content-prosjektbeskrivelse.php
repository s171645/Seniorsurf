		</p>
			
<hr/>	
	<h4><strong>Prosjektets problemstilling:</strong></h4>
		<p>
			Vår problemstilling vi skal jobbe med igjennom prosjektet er at vi skal produsere en lettfattelig og veldig brukervennlig hjemmeside for eldre som gjerne vil, men ikke helt har skjønt hva internett er. 
		</p>
			
			<strong>Vårt spørsmål som vi skal ”besvare” gjennom prosjektet er:</strong> 
				Hvordan kan vi gjøre det lettere for eldre å forstå og bruke internett?

