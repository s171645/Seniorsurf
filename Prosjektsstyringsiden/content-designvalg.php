
<h2>Begrunnelse for designvalg</h2>

<h4><strong>Type nettsted:</strong></h4>

	<p>
		Nettsiden vår skal være en hjelpeside for eldre. 
		Vi lager en svært enkel side som skal gi eldre en grunnleggende forståelse for hvordan man bruker internett.
		Vi lager en stilren side med sterke kontraster, som i tillegg skal være svært oversiktlig slik at brukere lett vil kunne navigere rundt uten å rote seg bort.
		Vi vil også bruke demonstrasjonsvideoer, dette fordi vi har tro på at det vil være lettere for eldre å se hva man
		faktisk skal gjøre, i stede for å prøve å forklare det i masse kjedelig tekst. 
	</p>	

<h4><strong>Målgruppe:</strong></h4>

	<p>
		Vår målgruppe er eldre mennesker som ikke er så veldig kjent med internett og hvordan man bruker det.
	</p>
		
<h4><strong>Behov:</strong></h4>

	<p>
		Med vår hjemmeside har vi fokus på en brukergruppe som har behov for en enkel forklaring på hva internett er
		og hva man kan bruke det til! Vi tenker at våre brukere vil ha et behov for en lettfattelig side
		uten masse forstyrrelser i form av avansert design og masse bilder,
		noe som kan gjøre navigeringen vanskeligere for nye brukere av internett.
		Vi har også større skrift på siden, dette pga at mange eldre har dårlig syn
		og vil derfor ha behov for større skrift som gjør teksten leselig for dem.
	</p>
	
<h4><strong>Tanker ved utforming av tekst:</strong></h4>

	<p>
		Vi vil ha en side uten som er lett lest og forståelig. 
		Noen ting med internett kan man skrive en hel doktoravhandling om,
		og det vil nok neppe appelere til en eldre brukergruppe som skal prøve å forstå feks basic søketeknikk.
		Vi vil derfor prøve å lage korte tekster, som forklarer det vi vil fram til på en best mulig måte.
		Siden brukergruppen vår er eldre mennesker, har vi tatt hensyn til svaksynte. Vi har en stor font,
		og vi har inkludert tre knapper med mulighet for å velge enda større skrift på teksten. 
		
	</p>