<h2>Gruppens reglement</h2>

	<p>

		•	Gruppen samles fredager 12:30 og eventuelle andre avtalte tidspunkt. Gruppens medlemmer må ha vært i kontakt med gruppen på skolen minst en gang i uka.<p>

		•	Hvis enkeltmedlemmer av til gruppen uteblir, gis det en advarsel.<p>

		•	Samtlige gruppemedlemmer gjør avtalte oppgaver til avtalt tidspunkt (eventuelle problemer skal meldes fra til gruppen snarest mulig)<p>

		•	Gjøres det større endringer, skal dette meldes ifra til resten av gruppen omgående. Er endringen av en betydelig karakter, skal dette tas opp i med gruppen i fellesskap før endringen utføres. <p>

		•	Det er enhver medlems ansvar å utføre sine oppgaver.<p>

		•	Samarbeidet skal være åpent og kritikk kan gis direkte, men skal være saklig.<p>

		•	Samtlige av gruppens medlemmer skal oppleve at de blir hørt og forstått.<p>

		•	Blir det diskusjoner og uenigheter om ting så skal alle gruppemedlemmer kontaktes og avtales tid for å møtes og komme til et løsning fremfor å bruke unødig tid.<p>

		•	Brudd på disse punktene kan føre til advarsler og senere utestenging. 3 advarsler så er man ute av gruppen.<p>


