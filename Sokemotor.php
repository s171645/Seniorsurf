			 <h2> Hvordan søke på Internett </h2>
				
			 <p>
Det finnes mange forskjellige måter vi kan bruke internett på, men majoriteten av de som bruker internett søker etter informasjon om noe. Det finnes vel ikke en ting man ikke kan finne informasjon om hvis man bare vet hvordan man leter. 
Så hvordan finner vi det vi leter etter?
På Internett har vi noe som kalles for søkemotorer. En søkemotor er en betegnelse for en type programvare som leter frem nettsider på Internett som samsvarer med det man søker etter, sorterer resultatene etter som hva den anser som mest relevant for ditt søk. Det finnes mange forskjellige søkemotorer, men de mest populære hos nordmenn er norske Kvasir, og de internasjonale søkemotorene Yahoo og Google (som er den som blir mest brukt verden over).
<br>

<br>En søkemotor kan gjøre søk på:<p>
- Hele Internett (Google og Yahoo),<p>
- Innenfor et bestemt nettsted (for eksempel søk innenfor Wikipedia)<p>
- Innenfor et bestemt tema (f.eks. Kelkoo, som søker etter priser på produkter, og Picsearch, som søker etter bilder).<p>
- En bedrift kan også sette opp en intern bedrifts- søkemotor for å få enklere tilgang til alle dokumenter og databaser i bedriften.<p>
<br>

Søketeknikker<p>
Oftest når man gjør et søk, har man vanligvis problemer med at man enten får for mange eller for få treff. Det er ikke bestandig lett å vite om et søk er for generelt eller for spesifikt, så det enkleste man kan gjøre er derfor og bare prøve, får så å velge taktikken videre basert på dette.
<p>
Når man skal søke ved hjelp av en søkemotor, oppgir man et ord eller et navn på det man er ute etter å finne informasjon om. For eksempel kan man søke etter «blomster» eller «norske blomster», hvis man vil begrense søket det til norske blomstersorter. Hvis søket man gjør fører til mange hundre eller tusen treff, er søkeordene for generelle! Da kan man prøve å være mer spesifikt i sitt søk ved og for eksempel skrive navnet på blomsterarten. Hvis man ikke får et eneste treff på søket sitt, er det gjerne for spesifikt igjen, og man må da prøve et litt mer generelt søk igjen. Dette høres kanskje innviklet ut, men man blir mer klok på dette med søking når man har prøvd det et par ganger.
<p>

