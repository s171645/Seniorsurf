Her kan du prøve ut det å søke litt forskjellige ting
<p>
Får du for mange treff kan du: 
<br>Forsøke å legge til flere ord. Hvis du bare har skrevet «kjøpe bil», er ikke det så veldig spesifikt. Dersom du er ute etter å kjøpe en Audi hos en forhandler i Oslo, så får man garantert svar på det man er ute etter dersom man søker på «Audi forhandler Oslo» i stede.
<br>Det kan lønne seg å bytte fra generelle til mer spesielle ord: Hvis du for eksempel har skrevet «Syden», men egentlig er interessert i å reise til Spania, så skriv «Reise til Spania» i stedet.
<br>Dersom du legger til ord du tror kan forekomme på sider som kan være relevante, kan dette hjelpe. Hvis du for eksempel skriver «chartertur» får du nok mest sannsynlig opp en del reisereferater fra privatpersoner, Hvis du legger til ord som «avreise», «pris» eller «bestill», er det nok mer sannsynlig at du får opp sider som tilbyr charterturer.
<br>Legg til "hermetegn" på begge sider av ordet. Når du legger til "hermetegn" på hver side av ordene du søker på, finner søkemotoren KUN de dokumentene der ordene forekommer nøyaktig i den rekkefølgen du har oppgitt.
<p>
Får du for få treff kan du: 
<br>Sjekke stavemåten. Har du brukt et vanlig ord som du regner med forekommer mange steder, så har du antagelig stavet det feil. Finn rett stavemåte og prøv igjen.<p>
<br>Selv om mennesker forstår at «merkelig» og «rar» betyr det samme, så forstår ikke søkemotorer vanligvis det. Prøv å bruke synonymer til ordet du leter etter.<p>
<br>Mesteparten av Internett er ikke på norsk. Med mindre ordet du søker etter er likt på norsk og engelsk, leter søkemotoren bare etter dokumenter på språket du har skrevet søkeordet på. Hvis du kan lese engelsk, kan du prøve å oversette søkeordet til engelsk og søke igjen.
<br>Hvis du har brukt "hermetegn" på begge sider av et søkeord, kan det være smart å fjerne "hermetegnene"-tegnene. Dette åpner for at sider der «Per Hansen» bare er nevnt som «Per» eller «Hansen» også kommer opp.<p>
<br>Det er ikke alltid at en nettside som er om noe, nevner det den omtaler. Prøv å søke etter ord du tror vil forekomme i siden du leter etter.
